#include "3-calc.h"
/**
 * op_add - adds two integers
 * @a: integer 1
 * @b: integer 2
 *
 * Return: sum of the two integers
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - adds two integers
 * @a: Integer 1
 * @b: Integer 2
 *
 * Return: subtraction of the two integers
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: product of integers
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides two numbers
 * @a: first number
 * @b: second number
 *
 * Return: Division of the two numbers
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - calculates remainder
 * @a: First integer
 * @b: Second integer
 *
 * Return: remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
